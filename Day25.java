public class Day25 {
    public static void main(String[] args) {
        // Operator increment
        int y = 14;
        System.out.println("Nilai awal y\t\t= " + y);
        y++;
        System.out.println("Setelah increment\t= " + y);

        // Operator decrement
        int g =19;
        System.out.println("Nilai awal g\t\t= "+g);
        g--;
        System.out.println("Setelah decrement\t= " + g);
    }
}
