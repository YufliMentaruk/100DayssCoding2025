public class Day93 {
    public static void main(String[] args) {

        double a =4.5;
        
        // Pembulatan Keatas
        System.out.println("=== Contoh Math.ceil() ===");
        System.out.println("ceil(4.5) = " + Math.ceil(a));
        // Pembulatan Kebawah
        System.out.println("\n=== Contoh Math.floor() ===");
        System.out.println("floor(4.5) = " + Math.floor(a));
        // Pembulatan Terdekat
        System.out.println("\n=== Contoh Math.round() ===");
        System.out.println("round(4.5) = " + Math.round(a));

    }
}
