public class Day15 {
    public static void main(String[] args) {
        // Deklarasi variabel
        int a = 14;
        int b = 19;

        // Operasi penjumlahan
        int Tambah = a + b;
        // Operasi pengurangan
        int Kurang = a - b;

        // Cetak hasil
        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);
        System.out.println("Hasil penjumlahan dari a + b = " + Tambah);
        System.out.println("Hasil pengurangan dari a - b = " + Kurang);
    }
}
