public class Day16 {
    public static void main(String[] args) {
        // Deklarasi variabel
        int a = 10;
        int b = 5;

        // Operasi perkalian
        int Kali = a * b;
        // Operasi pembagian
        int Bagi = a / b;

        // Cetak hasil
        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);
        System.out.println("Hasil perkalian dari a * b = " + Kali);
        System.out.println("Hasil pembagian dari a / b = " + Bagi);
    }
}
